export function getRelevantVideos(lessonTopic: string, apSubject?: string): YouTubeVideo[] {
  const topicWords = lessonTopic.toLowerCase().split(/\s+/);
  
  return youtubeVideos.filter(video => {
    // Match AP subject if provided
    const subjectMatch = !apSubject || video.apSubject.toLowerCase().includes(apSubject.toLowerCase());
    
    // Check if any topic words match video topics or description
    const topicMatch = topicWords.some(word => 
      video.topics.some(topic => topic.toLowerCase().includes(word)) ||
      video.description.toLowerCase().includes(word) ||
      video.title.toLowerCase().includes(word)
    );
    
    return subjectMatch && topicMatch;
  });
}